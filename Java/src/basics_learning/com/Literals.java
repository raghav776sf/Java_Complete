package basics_learning.com;

public class Literals {
    public static void main(String[] args) {
        byte age = 55;
        short test1 = 976;
        int test2 = 74568;
        long test3 = 9835478096439l;
        float test4 = 45.8f;
        double test5 = 678.98;
        boolean test6 = false;
        System.out.println(age);
    }
}

// Integral :- byte, short, int, long(l/L)
// Floats :- float(f/F), double(d/D)
// Char :- ('')
// Boolean :- false/true